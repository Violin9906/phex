      <h2>关于网站LOGO</h2>
      <p>
        网站的LOGO是<a href="http://scp-wiki-cn.wikidot.com/site-cn-91">SCP基金会中国分部设施Site-CN-91</a>的标志。根据基金会中国分部的设定，Site-CN-91于1958年在北京建立，对外伪装为██大学，1969年迁至██。基金会对其的解释是这样的：
      </p>
      <blockquote>
        <h3>关于Site-CN-91的原型</h3>
        是，正如你所想的，Site-CN-91毫无疑问是有确切的现实原型的。<br>
        “于1958年建立”、“受到中国科学院的支持”、“于1969搬迁”……没错，若是对新中国的科技史略有了解的话，便能看出其原型——中国科学技术大学。<br>
        中国科学技术大学于1958年建校，建校之初直属中国科学院。根据“全院办校、所系结合”的方针，中科大的第一批教授均由国内的顶尖科研人才担任。中科大原址位于北京，1969年迁至安徽合肥……凡此种种科大校史在此不再赘述。<br>
        总之，大可以猜想诸如钱学森、赵九章、华罗庚、郭永怀等科研先驱均是Site-CN-91的高层主管，也可以猜想Site-CN-91的研究员们在“两弹一星”等项目中有何建树（事实上真的有一篇文档与此有关）。<br>
        不过，这并不意味着Site-CN-91就是中科大。<br>
        确切来说，1969年之后的Site-CN-91可以是中科大，也可以不是。其真实身份是——那所大学。<br>
        没错，就是“那所大学”，是当谈到中国的大学时，读者诸君脑海中第一个想到的“那所大学”。<br>
        因为是“那所大学”，Site-CN-91可能是美女如云的大学生天堂，也可能是“男女比例七比一”的理工科单身狗窝；<br>
        因为是“那所大学”，Site-CN-91可能位于西子湖畔人间天堂，也可能位于鬼知道什么二线城市；<br>
        因为是“那所大学”，Site-CN-91的新晋研究员可能个个谢顶，也可能个个脱发；<br>
        因为是“那所大学”，Site-CN-91可能有着世界一流的全面学科，也可能只教授如何修电脑修汽车和开挖掘机；<br>
        因为是“那所大学”……<br>
        总之请诸位牢记：Site-CN-91毫无疑问是有其原型，其原型就是你对大学的憧憬，是你心中的“那所大学”。<br>
        欢迎来到Site-CN-91。<br>
      </blockquote>
